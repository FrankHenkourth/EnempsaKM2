    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int num = 1;
        String nombrePartida = jTextField1.getText();
        
        if(nombrePartida.trim().isEmpty()) JOptionPane.showMessageDialog(null, "El nombre del proyecto no puede estar vacio!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(nombrePartida.length() < 5) JOptionPane.showMessageDialog(null, "El nombre del proyecto debe de tener como minimo 5 caracteres!", "Error", JOptionPane.ERROR_MESSAGE);
        else {
            if(jRadioButton2.isSelected()) num = 2;
            if(jRadioButton3.isSelected()) num = 3;
            
            datos.setNumero(num); // Guarada el id del tamaño del mapa
            datos.setTexto(nombrePartida); // Guarda el nombre que tendra la partida
            
            dispose();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

